/**
 * Escape user-supplied values for safe interpolation into HTML email templates.
 * Prevents HTML injection / XSS in email clients.
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}
